name = "ishika"

# Convert to uppercase
uppercase_name = name.upper()
print("Uppercase:", uppercase_name)

# Convert to lowercase
lowercase_name = name.lower()
print("Lowercase:", lowercase_name)

# Convert to title case
title_case_name = name.title()
print("Title Case:", title_case_name)

# Print a message using string concatenation
message_concatenation = "Hello " + name + ", welcome"
print("Message with String Concatenation:", message_concatenation)

# Compare strings
comparison_result = name == "ishika"
print("Comparison Result:", comparison_result)

# String formatting
formatted_message = "Hello {}, welcome".format(name)
print("Formatted Message:", formatted_message)

# F-strings
f_string_message = f"Hello {name}, welcome"
print("F-String Message:", f_string_message)


